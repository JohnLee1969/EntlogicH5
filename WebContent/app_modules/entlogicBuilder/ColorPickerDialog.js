// 内部变量
var color = "";


/////////////////////////////////////////////////////////////////////////////////////////////////
// 内部函数


/**
 * 组件初始化
 */ 
function initComponents() {
	$("#divColorPicker").ColorPicker({
		flat: true,
		onSubmit: function(hsb, hex, rgb, el) {
			color = "#" + hex;
			parent.dialogCallBack(color);
		},
		onBeforeShow: function () {
			$(this).ColorPickerSetColor(color);
		}
	})
	
	$("#btnOK").click( btnOK_onClick);
};


